package com.example.demo.pojo;
//新增
public class Proj_record {
	int id;
	String name; //项目名称
	String projNum;//项目编号
	String reqNum;//申报编号
	String status;//状态
	String numberRecord;//档案编号
	String numberRecordHall;//档案馆编号
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getProjNum() {
		return projNum;
	}
	public void setProjNum(String projNum) {
		this.projNum = projNum;
	}
	public String getReqNum() {
		return reqNum;
	}
	public void setReqNum(String reqNum) {
		this.reqNum = reqNum;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getNumberRecord() {
		return numberRecord;
	}
	public void setNumberRecord(String numberRecord) {
		this.numberRecord = numberRecord;
	}
	public String getNumberRecordHall() {
		return numberRecordHall;
	}
	public void setNumberRecordHall(String numberRecordHall) {
		this.numberRecordHall = numberRecordHall;
	}
	
}
